
public class Farmer2
{
	private int xPos;
    private int yPos;
    public Farmer2(){
        xPos = 0;
        yPos = 0;
        
    }
    
    public int getxPos() {
    	return xPos;
    }
    public int getyPos() {
    	return yPos;
    }
    public void setxPos(int a) {
    	xPos = a;
    }
    public void setyPos(int a) {
    	yPos = a;
    }
    
    
}
